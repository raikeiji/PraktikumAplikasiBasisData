    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        barang b = new barang();
        kategoribarang kabar = new kategoribarang();

        int i = jComboBoxKategoriBarang.getSelectedIndex();
        String kodekategori = jComboBoxKategoriBarang.getItemAt(i).toString();
        String[] temp = kodekategori.split(" ");
        kabar.setKode_kategori(temp[0]);
        kabar.setNama_kategori(temp[1]);

        b.setBeliSebelum(Double.parseDouble(jTextFieldBeliSebelum.getText()));
        b.setBeliSesudah(Double.parseDouble(jTextFieldBeliSetelah.getText()));
        b.setKategori(kabar);
        b.setKodeBarang(jTextFieldKodeBarang.getText());
        b.setNamaBarang(jTextFieldNamaBarang.getText());
        b.setQuantityMax(Double.parseDouble(jTextFieldQtyMax.getText()));
        b.setQuantityMin(Double.parseDouble(jTextFieldQtyMin.getText()));
        b.setRitelSebelum(Double.parseDouble(jTextFieldRitelSebelum.getText()));
        b.setRitelSesudah(Double.parseDouble(jTextFieldRitelSetelah.getText()));
        b.setSatuan(jComboBoxSatuan.getSelectedItem().toString());
        b.setGambar(jLabelGambar.getText());
        if (jButtonSimpan.getText().equals("Simpan")) {
            try {
                BarangKontrol.getKoneksi().tambahKategori(b);
                JOptionPane.showMessageDialog(rootPane, "DATA TELAH MASUK KE DATABASE");
            } catch (SQLException ex) {
                Logger.getLogger(daftarmenuArsip.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                BarangKontrol.getKoneksi().updateBarang(b);
                JOptionPane.showMessageDialog(rootPane, "DATA TELAH DIUPDATE KE DATABASE");
            } catch (SQLException ex) {
                Logger.getLogger(daftarmenuArsip.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButtonSimpanActionPerformed

