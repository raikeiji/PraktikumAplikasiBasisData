    private void jButtonCetakFrameTampilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCetakFrameTampilActionPerformed
        if (jCheckBoxCetakFrame.isSelected()) {
            try {
                List<barang> listKategori = BarangKontrol.getKoneksi().lihatSeluruhKateogri();
                TabelModelBarang model = new TabelModelBarang(listKategori);
                jTableJInternalFrameCetakBarang.setModel(model);
            } catch (SQLException ex) {
                Logger.getLogger(daftarmenuArsip.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            String kolomAwal = jTextFieldAwalCetakFrame.getText();
            String kolomAkhir = jTextFieldAkhirCetakFrame.getText();
            try {
                List<barang> listKategori = BarangKontrol.getKoneksi().TampilTabelCetak(kolomAwal, kolomAkhir);
                TabelModelBarang model = new TabelModelBarang(listKategori);
                model.fireTableDataChanged();
                jTableJInternalFrameCetakBarang.setModel(model);
            } catch (SQLException ex) {
                Logger.getLogger(daftarmenuArsip.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonCetakFrameTampilActionPerformed

